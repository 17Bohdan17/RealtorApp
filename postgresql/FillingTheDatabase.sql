
-- Створення таблиці Object 
CREATE TABLE Object (
    Object_id SERIAL PRIMARY KEY,
    Street VARCHAR(255),
    Street_num INT,
    Area FLOAT,
    Price FLOAT,
    Status VARCHAR(50),
    Room_count INT
);

--Створення таблиці 
CREATE TABLE Facilities (
    Facility_id SERIAL PRIMARY KEY,
    Object_reference_id INT,
    Min_bedrooms INT,
    Min_bathrooms INT,
    Garage BOOLEAN,
    Garden BOOLEAN,
    Pool BOOLEAN,
    FOREIGN KEY (Object_reference_id) REFERENCES Object(Object_id)
);

-- Створення таблиці Requirement 
CREATE TABLE Requirement (
    req_id SERIAL PRIMARY KEY,
    req_min_bedrooms SMALLINT NOT NULL,
    req_min_bathrooms SMALLINT NOT NULL,
    req_type VARCHAR NOT NULL,
    req_minimal_area FLOAT NOT NULL,
    req_max_price MONEY NOT NULL,
    req_street VARCHAR(30) NOT NULL,
    req_garage BOOLEAN NOT NULL,
    req_garden BOOLEAN NOT NULL,
    req_pool BOOLEAN NOT NULL
);

-- Створення таблиці Client
CREATE TABLE Client (
    client_id SERIAL PRIMARY KEY,
    first_name VARCHAR(30) NOT NULL,
    second_name VARCHAR(30) NOT NULL,
    contact_num BIGINT NOT NULL,
    req_id INTEGER REFERENCES Requirement(req_id) ON DELETE CASCADE
);

-- Створення таблиці Agreement 
CREATE TABLE Agreement (
    agreement_id SERIAL PRIMARY KEY,
    object_id INTEGER REFERENCES Object(object_id) ON DELETE CASCADE,
    client_id INTEGER REFERENCES Client(client_id) ON DELETE CASCADE,
    agreement_date TIMESTAMP NOT NULL,
    agreement_price INT NOT NULL,
    agreement_status VARCHAR NOT NULL
);

-- Створення таблиці Consultation 
CREATE TABLE Consultation (
    cons_id SERIAL PRIMARY KEY,
    client_id INTEGER REFERENCES Client(client_id) ON DELETE CASCADE,
    cons_date TIMESTAMP NOT NULL,
    cons_status VARCHAR NOT NULL
);

